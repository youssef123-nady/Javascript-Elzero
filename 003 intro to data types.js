// get the type of the data using "typeof()" method 
console.log(typeof(true));       // boolean 
console.log(typeof(false));      // boolean 
console.log(typeof('osama'));    // string
console.log(typeof(120));        // number
console.log(typeof(120.1));      // number
console.log(typeof([10, 20, true, 'osama']));       // object --> array 
console.log(typeof({name: "Youssef", age: 22, isStudent: false}));      // object --> dictionary
console.log(typeof(undefined));     // undefined
console.log(typeof(null));      // object