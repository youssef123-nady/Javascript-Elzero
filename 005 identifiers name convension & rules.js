/*
    =============================================
    == 005 identifiers name convension & rules == 
    =============================================
    - spaces is not valid in identifiers
    - can not start with numbers in identifiers
    - can not include special characters on identifiers
    - can include numbers, dollar sign, and under score on identifiers
    - identifiers are case sensitive 
    - we can not use reserved key words like: let, var, function, while, for, if, try, ... 
    - when name the identifiers we write using camelCase "firstName" "lastName"
*/
let firstName = 'osama';
let lastName = 'mohamed';

console.log(firstName);     // osama
console.log(lastName);      // mohamed