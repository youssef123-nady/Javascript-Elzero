/*
    ==============================
    == 018 comparison operators ==
    ==============================
    ==      equal 
    !=      not equal 
    ===     identical 
    !==     not identical 
    >       larger than 
    >=      larger than or equal 
    <       smaller than 
    <=      smaller than or equal 
*/

// compare only the value 
console.log(10 == "10");          // true 
console.log(-10 == "-10");        // true 

// compare the value and data type 
console.log(10 === "10");        // false

console.log(10 != 10);      // false

console.log(10 >= 5);       // true